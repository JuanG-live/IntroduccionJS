// TIPOS DE DATOS

/* UNDEFINED - This type is used to represent a variable that has been declared but has not yet been assigned a value.
let client
console.log(client) // undefined
console.log(typeof client) // undefined
*/

/* String or change text - This type is used to represent text values. Strings can be enclosed in single or double quotes.
let client = "Juan"
console.log(client) // Juan
console.log(typeof client) // string
const client2 = 'John McClane'
console.log(client2) // John McClane
console.log(typeof client2) // string
*/


/* NUMBERS - This type is used to represent numeric values, both integers and decimals.
const price = 10
const price2 = 10.5
const price3 = -10
console.log(price) // 10
console.log(price2) // 10.5
console.log(price3) // -10
console.log(typeof price) // number
console.log(typeof price2) // number
console.log(typeof price3) // number
*/

/* BOOLEAN - This type is used to represent logical values, which can be either true or false.
const isTrue = true
const isFalse = false
console.log(isTrue) // true
console.log(isFalse) // false
console.log(typeof isTrue) // boolean
console.log(typeof isFalse) // boolean
*/

/* NULL - This type is used to represent a variable that has been explicitly assigned a null value.
const client = null
console.log(client) // null
console.log(typeof client) // object (this is a known bug in JavaScript)
*/

